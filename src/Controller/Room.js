function Room( Hotel_ID, RoomType_Description, RoomType_Name, Room_Date, Room_ID, Room_Max_GuestNum, Room_Price ) {
	this .Hotel_ID = Hotel_ID;
	this .RoomType_Description = RoomType_Description;
	this .RoomType_Name = RoomType_Name;
	this .Room_Date = Room_Date;
	this .Room_ID = Room_ID;
	this .Room_Max_GuestNum = Room_Max_GuestNum;
	this .Room_Price = Room_Price;
}
